// -- localStorage persistence layer --

function loadProducts(): Product[] {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw) return JSON.parse(raw);
  } catch {
    // Corrupted data — reset
  }
  // First load: seed from hardcoded data
  localStorage.setItem(STORAGE_KEY, JSON.stringify(SEED_PRODUCTS));
  return [...SEED_PRODUCTS];
}

function persistProducts(products: Product[]): void {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(products));
}

// -- Public API --

export function getAllMockProducts(): Product[] {
  return loadProducts();
}

export function getMockProductsByVendorId(vendorId: string): Product[] {
  return loadProducts().filter((p) => p.vendorId === vendorId);
}

export function getMockProductById(productId: string): Product | undefined {
  return loadProducts().find((p) => p.id === productId);
}

export function saveMockProduct(product: Product): void {
  const products = loadProducts();
  const idx = products.findIndex((p) => p.id === product.id);
  if (idx >= 0) {
    products[idx] = product;
  } else {
    products.push(product);
  }
  persistProducts(products);
}

export function deleteMockProduct(productId: string): void {
  const products = loadProducts().filter((p) => p.id !== productId);
  persistProducts(products);
}
